public class Restaurant {
    // Attributes
    String name; // Name, displayed on receipt
    String num; // Phone number, displayed on receipt
    String location; // City, displayed on receipt

    // Methods
    // Constructor
    Restaurant (String name, String phone, String city) {
        this.name = name;
        this.num = phone;
        this.location = city;
    }

    // Returns location, used to find driver
    String getLocation() {
        return location;
    }
}